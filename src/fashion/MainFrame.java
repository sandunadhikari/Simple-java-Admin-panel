    public ImageIcon Resize(String imagePath,byte [] pic){
        ImageIcon img=null;
        if(imagePath!=null){
            img=new ImageIcon(imagePath);
        }else{
            img=new ImageIcon(pic);
        }
        Image myImage=img.getImage();
        Image myImage1=myImage.getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(myImage1);
        return image;
    }
    public boolean chekInput(){
        if(txt_name.getText()==null||txt_price.getText()==null||txt_adddate.getDate()==null){
            return false;
        }else{
            try {
               Float.parseFloat(txt_price.getText());
                return true; 
            } catch (Exception e) {
                return false;
            }
            
        }
    }
    public ArrayList<Items> getItem(){
        ArrayList<Items> itemList=new ArrayList<Items>();
        Connection con=getConnection();
        String quary="SELECT * FROM products";
        Statement st;
        ResultSet rs;
        try {
            st=con.createStatement();
            rs=st.executeQuery(quary);
            Items items;
            while (rs.next()) {
               items=new Items(rs.getInt("Id"), rs.getString("Name"),Float.parseFloat(rs.getString("Price")) , rs.getString("Add_date"), rs.getBytes("Image"));
               itemList.add(items);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return itemList;
    }
    public void Show_Products_In_Table(){
        ArrayList<Items> list=getItem();
        DefaultTableModel model=(DefaultTableModel)jTable_item.getModel();
        // clear table content
        model.setRowCount(0);
        Object[] row=new Object[4];
        for (int i = 0; i < list.size(); i++) {
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getPrice();
            row[3]=list.get(i).getAddDate();
            
            model.addRow(row);
        }
    }
