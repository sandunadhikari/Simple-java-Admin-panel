    public void ShowItem(int index){
        txt_id.setText(Integer.toString(getItem().get(index).getId()));
        txt_name.setText(getItem().get(index).getName());
        txt_price.setText(Float.toString(getItem().get(index).getPrice()));
        
        try {
            Date addDate=null;
            addDate =new SimpleDateFormat("yyyy-MM-dd").parse((String)getItem().get(index).getAddDate());
            txt_adddate.setDate(addDate);
        } catch (ParseException ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        lbl_image.setIcon(Resize(null, getItem().get(index).getImage()));
    }
